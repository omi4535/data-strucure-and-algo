#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a,b;
    cin>>a>>b;
    cout<<"AND operator \n a&b = "<<(a&b)<<endl<<endl;
    cout<<"OR operator \n a&b = "<<(a|b)<<endl<<endl;
    cout<<"XOR operator \n a&b = "<<(a^b)<<endl<<endl;
    cout<<"right shift \n(a>>2) = "<<(a>>2)<<endl<<endl;
    cout<<"left shift \n(a<<2) = "<<(a<<2)<<endl<<endl;
    cout<<"not of a\n"<<(~a)<<endl<<endl;

    return 0;
}
